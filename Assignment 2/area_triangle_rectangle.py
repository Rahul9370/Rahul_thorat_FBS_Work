# 4. WAP to calculate area of triangle and rectangle

# area of triangle
base = int(input("Enter a base of   triangle:"))
height = int(input("Enter height of triangle:"))

area_triangle = (1/2)  * base * height

print("Area of triangle is:",area_triangle)


# area of rectangle
length = int(input("\n\nEnter lenght of rectangle:"))
width = int(input("Enter width of rectangle:"))

area_rectangle = length * width

print("Area of Rectangle is:",area_rectangle)